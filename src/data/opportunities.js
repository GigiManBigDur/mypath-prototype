// OPPORTUNITIES[track][level] -> 3-4 cards.
// date: {month, day} is a template date (see src/utils/dates.js — anchored to "today" at
// generation time), or {offsetDays: N} for an explicit real-day offset from today (used to
// deliberately place an item in the past). prepWeeks: how many weeks before the deadline the
// prep chain's window opens. prepSteps: sequential sub-task names shown as a connected chain
// leading up to the deadline/event node. stepResources (parallel array, same length as
// prepSteps) supplies each individual step's own resources — not just the first step — so a
// student clicking any step in the middle of a chain sees something real behind it, not just a
// name. A step with no genuine external resource (a purely local/personal action like "Attend
// your first meeting" or "Election day") gets `[]` rather than a padded/fake one, same judgment
// call as the empty `resources` on purely reflective core tasks in trunkSteps.js. Escalation-year
// steps (progressionPrepSteps, year 2+) and custom user-created tasks are out of scope for this —
// see CLAUDE.md.
//
// `recurring: true` + `progressionType` marks club/competition-style opportunities that repeat
// every year of a multi-year plan instead of appearing once — roadmapGenerator.js generates one
// escalating instance per year-stage the plan spans (a 12th-grader still gets exactly one
// instance, same as before this existed). Year 1 always uses the opportunity's own prepSteps/
// date data completely unmodified; PROGRESSION_LADDERS below only supplies the escalated final-
// step title for year 2 onward (index 0 = year 2), so there's no need to touch year-1 wording
// for most entries — only a handful where the natural last prepStep didn't already read as a
// real "compete/attend" milestone got that step's wording adjusted (see e.g. `deca`, `hosa`).
// One-time programs (scholarships, single research placements, honor societies, internships)
// are NOT tagged recurring and keep the original one-chain-in-its-nearest-year behavior exactly.
//
// UC Davis "My School" clubs (`ucdc-*` ids, `schoolVerified: true, schoolName: 'UC Davis'`) —
// real, independently-fetched club data from aggielife.ucdavis.edu/club_signup?view=all&, UC
// Davis's own club directory (~880 groups total). Deliberately a curated ~70-club sample, not the
// full directory — 7 of the build spec's own listed AggieLife categories (Academic/Honors,
// Advocacy and/or Political, Arts and Entertainment, Community Service, Dance Performance,
// Environmental/Sustainability, Ethnic/Cultural/Identity-Based, Health and Wellness, Professional,
// Recreation/Sports, Religious/and or Spiritual — 11 total) each contributed 7 clubs, selected by
// real mission-text substance (a genuine quality signal, not cherry-picked by name) and swapped
// away from 2 grad-student-only orgs that surfaced in that selection, toward undergrad-inclusive
// replacements — a UC-Davis-scoped audience shouldn't be steered toward the tiny share of clubs
// students at that level can't actually join. Purely administrative entries (`Student Affairs
// Department` / `ASUCD` org types — e.g. the Center for Student Involvement's own admin listing)
// were excluded before selection ever ran; no `DO NOT REACTIVATE` category actually appeared in
// the fetched page (own club-level tags never carry it — it's a dead filter option). Each club's
// own real category then routed it to whichever app track fits its ACTUAL content (e.g. AIAA/
// AIChE/ASM → stem; Davis Rewilding Society/Vintage Club → outdoors; identity/cultural/religious
// affinity clubs with no genuine skill/career angle → GENERIC_OPPORTUNITIES, same "don't force a
// fit" precedent the 4 unmapped Roslyn clubs already established) — occasionally overriding
// AggieLife's own category tag when the real content clearly pointed elsewhere (Davis Historical
// Fencing Club is tagged "Dance Performance" on AggieLife but is a martial-arts club here; Radio
// Association of Davis is tagged "Recreation/Sports" but is routed to `stem` as an amateur-radio/
// electronics club). Every entry appears in BOTH that track's `undergraduate` and `transfer`
// arrays (same content, same id, in both) since UC Davis's real clubs are equally open to either
// education level — unlike the rest of this file's undergraduate-vs-transfer split, there's no
// genuine content difference to write twice for a club (ids can safely repeat across the two
// arrays since a given plan only ever reads one `educationLevel` at a time). Each entry's
// `description` is a real, honest 1-3 sentence excerpt of that club's own fetched mission text
// (trimmed to a sentence boundary, matching this file's existing description length elsewhere —
// never invented or paraphrased), plus a `website` field (the club's real external site) rendered
// as plain text in OpportunityFinderScreen's `.card-meta` grid, alongside `howToApply`. Checked
// for genuine overlap with this file's own pre-existing generic/templated opportunities before
// writing any of this (Model UN, ASUCD/student government, campus newspaper/radio, DECA — none of
// which turned up as a real UC Davis club in the fetched data) — none of the selected 77 clubs
// duplicated an existing entry closely enough to honestly merge, so unlike Roslyn's 7 enrichments,
// this batch is 77 new entries and zero enrichments; forcing a merge where the content doesn't
// really match would be a worse outcome than the honest "checked, found none" here.
