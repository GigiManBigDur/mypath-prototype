// Merged, de-duplicated opportunity list for the given built tracks + education level.
// Falls back to the generic list when no built track is selected.
export function getOpportunityPool(tracks, level) {
  if (!tracks.length) return GENERIC_OPPORTUNITIES[level] || [];
  const seen = new Set();
  const merged = [];
  for (const t of tracks) {
    for (const o of OPPORTUNITIES[t]?.[level] || []) {
      if (!seen.has(o.id)) {
        seen.add(o.id);
        merged.push(o);
      }
    }
  }
  return merged;
}

export function findOpportunity(id, tracks, level) {
  const fromTracks = getOpportunityPool(tracks, level).find((o) => o.id === id);
  if (fromTracks) return fromTracks;
  // Falls back to GENERIC_OPPORTUNITIES even when tracks.length > 0 — getOpportunityPool's own
  // generic-fallback branch only runs when tracks is EMPTY, but a "My School" selection (see
  // getSchoolOpportunities below) can resolve to an unmapped/general real opportunity (e.g. an
  // identity/affinity club with no clean track fit, stored in GENERIC_OPPORTUNITIES.highschool)
  // regardless of how many real tracks the student's own interests otherwise mapped to. Without
  // this, that selection would silently never appear on the roadmap — the same "an out-of-scope
  // selection silently vanishes" bug class already fixed elsewhere in this codebase for Browse
  // mode (see CLAUDE.md).
  return (GENERIC_OPPORTUNITIES[level] || []).find((o) => o.id === id) || null;
}

// "My School" (Opportunity Finder's third view, alongside Recommended/Browse) — every real,
// independently-fetched, `schoolVerified` opportunity for the given school, regardless of which
// track's array it physically lives in. Deliberately scans EVERY track in OPPORTUNITIES (not just
// the student's own interest-derived tracks, and not just OPPORTUNITY_TRACKS) plus
// GENERIC_OPPORTUNITIES, since "what does my actual school offer" is a different lens than
// interest-based recommendation — a Roslyn student should see the real Forensics/DECA/Key Club
// entries here regardless of which interest tags they happened to pick on the Survey. Six of these
// entries are enrichments of a pre-existing generic opportunity (see the "Enriched with real
// Roslyn-specific info" comments on deca/science-olympiad/key-interact-club/student-government-hs/
// school-media-club/speech-debate-nsda/nhs above) rather than duplicates — merging into the
// existing entry, not adding a second listing, was the explicit build requirement.
export function getSchoolOpportunities(schoolName, level) {
  if (!schoolName) return [];
  const seen = new Set();
  const results = [];
  const collect = (list) => {
    for (const o of list || []) {
      if (o.schoolVerified && o.schoolName === schoolName && !seen.has(o.id)) {
        seen.add(o.id);
        results.push(o);
      }
    }
  };
  for (const track of Object.keys(OPPORTUNITIES)) {
    collect(OPPORTUNITIES[track]?.[level]);
  }
  collect(GENERIC_OPPORTUNITIES[level]);
  return results;
}
