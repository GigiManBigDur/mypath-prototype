export function getCourseById(id) {
  return COURSES.find((c) => c.id === id) || null;
}

// Simple case-insensitive substring search across course name (and department, so "Chemistry"
// also surfaces Science courses even if the word isn't in every matching course's own name) — the
// only search this app does; there's no free-text course entry anywhere, matching the "select
// from the real catalog" requirement.
export function searchCourses(query) {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return COURSES.filter((c) => c.name.toLowerCase().includes(q) || c.department.toLowerCase().includes(q));
}
