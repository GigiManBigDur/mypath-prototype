// Roslyn High School's full course catalog (2026-27), parsed directly from the school's own PDF
// (resources.finalsite.net/.../CourseCatalog2026-27.pdf) — one shared database used by both the
// Transcript & GPA screen (Course Selection Stage 2) and Course Selection itself (Stage 3), not
// duplicated per screen. Covers every department the build spec named: Art, Business, English,
// Math, Music & Theater, Physical Education & Health, Science, Social Studies, World Languages,
// 21st Century Learning (Robotics/AI/Computer Science/Media & Communication/PLTW Engineering),
// and Special Education. English as a New Language and BOCES-Barry Tech were NOT in that list and
// are deliberately excluded, not missed.
//
// Each course:
//   id             — stable slug (department + name), used as the value TranscriptScreen stores
//                    per entered course — never derived from array position.
//   name           — Title Case for display; the catalog itself uses ALL CAPS section headers, so
//                    names here are re-cased (acronyms/course-codes like AP, RSH, PLTW, DDP/IED,
//                    AB/BC preserved; already-stylized text like "INCubator@RHS" and parentheticals
//                    the catalog itself wrote in mixed case, e.g. "(pending Board approval)", left
//                    untouched rather than re-cased).
//   department     — one of the 11 named above.
//   credit         — numeric credit value (0.5, 1, 2...), or null when the catalog itself states
//                    none (see `note` on the 9 Special Education self-contained/subject-specific
//                    courses below — those are IEP-driven placements with no published fixed
//                    credit or grade range, and are NOT guessed here).
//   gradeLevels    — array of eligible grade levels per the catalog (e.g. [9, 10, 11, 12]), or []
//                    when not stated (same Special Education cases as above). This is course
//                    metadata (who CAN take it), not a transcript entry's own "year taken" field —
//                    that's separate, user-entered data on TranscriptScreen.
//   prerequisite   — plain-text prerequisite/corequisite as stated in the catalog, or null.
//   description    — a simplified 1-2 sentence summary, generated from the course's own real
//                    catalog description (not paraphrased/invented) and trimmed to a short length;
//                    a trailing "..." means it was cut short, not that content is missing.
//   weightCategory — 'ap' | 'research_honors' | 'honors' | 'standard', tagged from the course's own
//                    name per the catalog's stated rule (AP courses -> AP weight; RSH/Research
//                    Honors -> Research Honors weight; Honors -> Honors weight; everything else,
//                    i.e. Regents/100-point/Pass-Fail -> standard weight). See WEIGHT_MULTIPLIERS
//                    below for the actual numeric multipliers this maps to.
//   note           — present only on the 9 Special Education courses described above; explains why
//                    credit/gradeLevels are null/empty instead of guessed.
//   isPassFail     — present (true) only on the 2 courses whose own catalog description explicitly
//                    states they're graded pass/fail ("College Preparatory Strategies for SAT/ACT
//                    Exams", cross-listed under English and Math) — excluded from both the
//                    unweighted and weighted GPA per the catalog's own stated exclusion rule (see
//                    src/utils/gpa.js). Absent (undefined) on every other course, not false, since
//                    the catalog doesn't explicitly confirm every other course is numerically
//                    graded the same deliberate way it called these two out.
//
// A handful of courses are deliberately duplicated in TWO departments where the real catalog
// cross-lists them (e.g. "College Preparatory Strategies for SAT/ACT Exams" appears under both
// English and Math) — that's the source's own structure, not an extraction error.
